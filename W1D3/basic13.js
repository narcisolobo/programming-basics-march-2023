// Print 1-255
// Print all the integers from 1 to 255.
function print1To255() {
  for (var i = 1; i <= 255; i++) {
    console.log(i);
  }
}

print1To255();

// Print Sum 0-255
// Print integers from 0 to 255, and with each integer print the sum so far.
function printSum() {
  var sumSoFar = 0;
  for (var i = 0; i < 256; i++) {
    sumSoFar += i;
    console.log(i + ': ' + sumSoFar);
  }
}

printSum();

